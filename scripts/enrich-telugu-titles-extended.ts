async function enrichTeluguTitlesExtended() {
  console.log('=== ENRICHING TELUGU TITLES (EXTENDED) ===\n');
  
  // Get movies missing Telugu title
  const { data: movies, error } = await supabase
    .from('movies')
    .select('id, title_en, release_year')
    .eq('is_published', true)
    .is('title_te', null);
  
  if (error || !movies) {
    console.error('Error fetching movies:', error);
    return;
  }
  
  console.log(`üìä Movies missing Telugu title: ${movies.length}\n`);
  
  let updated = 0;
  
  for (const movie of movies) {
    // Try exact match first
    let teluguTitle = TELUGU_TITLES[movie.title_en];
    
    // Try with variations
    if (!teluguTitle) {
      // Remove special characters and try again
      const cleanTitle = movie.title_en.replace(/[:\-‚Äì]/g, ' ').replace(/\s+/g, ' ').trim();
      teluguTitle = TELUGU_TITLES[cleanTitle];
    }
    
    // Try partial match for titles with suffixes
    if (!teluguTitle) {
      for (const [key, value] of Object.entries(TELUGU_TITLES)) {
        if (movie.title_en.startsWith(key) || key.startsWith(movie.title_en)) {
          teluguTitle = value;
          break;
        }
      }
    }
    
    if (teluguTitle) {
      const { error: updateError } = await supabase
        .from('movies')
        .update({ title_te: teluguTitle, updated_at: new Date().toISOString() })
        .eq('id', movie.id);
      
      if (!updateError) {
        console.log(`‚úì ${movie.title_en} ‚Üí ${teluguTitle}`);
        updated++;
      }
    }
  }
  
  console.log(`\n=== SUMMARY ===`);
  console.log(`Total updated: ${updated}`);
  console.log(`Remaining: ${movies.length - updated}`);
}

