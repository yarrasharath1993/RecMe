// Type-safe translation getter
export type TranslationKey = keyof typeof translations;
export type TranslationSection<K extends TranslationKey> = keyof typeof translations[K];

/**
 * Get a translation value
 */
export function t<K extends TranslationKey, S extends TranslationSection<K>>(
  section: K,
  key: S,
  lang: Language
): string {
  const value = translations[section][key];
  if (value && typeof value === 'object' && lang in value) {
    return (value as Record<Language, string>)[lang];
  }
  return String(key);
}

/**
 * Format time ago string
 */
export function formatTimeAgo(dateString: string, lang: Language): string {
  const date = new Date(dateString);
  const now = new Date();
  const diffMs = now.getTime() - date.getTime();
  const diffMins = Math.floor(diffMs / 60000);
  const diffHours = Math.floor(diffMins / 60);
  const diffDays = Math.floor(diffHours / 24);

  if (diffMins < 1) {
    return translations.ui.justNow[lang];
  } else if (diffMins < 60) {
    return lang === 'te' 
      ? `${diffMins} ${translations.ui.minutesAgo.te}`
      : `${diffMins} ${translations.ui.minutesAgo.en}`;
  } else if (diffHours < 24) {
    return lang === 'te'
      ? `${diffHours} ${translations.ui.hoursAgo.te}`
      : `${diffHours} ${translations.ui.hoursAgo.en}`;
  } else {
    return lang === 'te'
      ? `${diffDays} ${translations.ui.daysAgo.te}`
      : `${diffDays} ${translations.ui.daysAgo.en}`;
  }
}
